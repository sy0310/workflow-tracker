-- 测试最小更新操作
-- 这个脚本测试最简单的更新操作，不涉及复杂的字段

-- 1. 查看项目19的当前状态
SELECT 
    id,
    项目名称,
    状态,
    负责人
FROM "活动策划" 
WHERE id = 19;

-- 2. 执行最简单的更新（只更新状态）
UPDATE "活动策划" 
SET 状态 = 2 
WHERE id = 19;

-- 3. 验证更新结果
SELECT 
    id,
    项目名称,
    状态,
    负责人
FROM "活动策划" 
WHERE id = 19;

-- 4. 测试包含多个字段的更新
UPDATE "活动策划" 
SET 
    项目名称 = '测试更新',
    状态 = 1,
    负责人 = '测试负责人'
WHERE id = 19;

-- 5. 最终验证
SELECT 
    id,
    项目名称,
    状态,
    负责人
FROM "活动策划" 
WHERE id = 19;
